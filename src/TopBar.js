  state = {
    anchorEl: null,
    mobileMoreAnchorEl: null,
    searchValue: '',
    single: '',
    popper: '',
    suggestions: [],  
    checkedA: true,
  };


  handleSuggestionsFetchRequested = ({ value }) => {
    this.setState({
      suggestions: getSuggestions(value),
    });
  };

  handleSuggestionsClearRequested = () => {
    this.setState({
      suggestions: [],
    });
  };

  handleChange = name => (event, { newValue }) => {
    this.setState({
      [name]: newValue,
    });
  };

